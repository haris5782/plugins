$pvsql = "SELECT * FROM `plugins_vars`";
$pvquery = $dbs->query($pvsql);

if ($pvquery->num_rows > 0)
{
	while ($pvrow = $pvquery->fetch_assoc())
	{
		$conf[$pvrow['name']] = $pvrow['value'];
	}
}

$_SESSION['plugins_conf'] = $conf;
